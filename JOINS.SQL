--JOINS USED TO COMBINE ROWS FROM TWO OR MORE TABLES
--NEED A COMMON COLUMN TO JOIN THE TABLES
--SAME DATA TYPES MATCHES WE CAN JOIN

--INNER JOIN (ONLY MATCHING ROWS FROM BOTH THE TABLES)

SELECT EMPLOYEE.EMP_ID,DEPARTMENT.DEPT_NAME FROM EMPLOYEE INNER JOIN DEPARTMENT ON EMPLOYEE.DEPT_ID = DEPARTMENT.DEPT_ID;
SELECT EMPLOYEE.EMP_NAME,DEPARTMENT.DEPT_ZONE FROM EMPLOYEE INNER JOIN DEPARTMENT ON EMPLOYEE.DEPT_ID = DEPARTMENT.DEPT_ID;
SELECT EMPLOYEE.DEPT_ID ,DEPARTMENT.DEPT_ID FROM EMPLOYEE INNER JOIN DEPARTMENT ON EMPLOYEE.EMP_ID = DEPARTMENT.DEPT_ID;

--LEFT OUTER JOIN(ALL THE ROWS FROM THE LEFT MATCHING ROWS FROM THE RIGHT SIDE)
SELECT EMPLOYEE.EMP_ID,DEPARTMENT.DEPT_NAME FROM EMPLOYEE LEFT OUTER JOIN DEPARTMENT ON EMPLOYEE.DEPT_ID =DEPARTMENT.DEPT_ID;
SELECT EMPLOYEE.EMP_NAME,DEPARTMENT.DEPT_ZONE FROM EMPLOYEE LEFT OUTER JOIN DEPARTMENT ON EMPLOYEE.EMP_ID = DEPARTMENT.DEPT_ID;

--RIGHT OUTER JOIN(ALL THE ROWS FROM RIGHT MATCHING ROWS FROM LEFT SIDE)
SELECT EMPLOYEE.EMP_ID,DEPARTMENT.DEPT_NAME FROM EMPLOYEE RIGHT OUTER JOIN DEPARTMENT ON EMPLOYEE.DEPT_ID = DEPARTMENT.DEPT_ID;

--FULL OUTER JOIN (IF MATHCS IT WILL JOIN IF NO MATCH ITS SHOWS NULL ON MISSING SIDE)
SELECT EMPLOYEE.EMP_ID,DEPARTMENT.DEPT_NAME FROM EMPLOYEE FULL OUTER JOIN DEPARTMENT ON EMPLOYEE.EMP_ID = DEPARTMENT.DEPT_ID;

--NATURAL JOIN AUTOMATICALLY JOINS TABLES USING COLUMNS WITH SAME NAME AND SAME DATA TYPE
-- YOU DO NOT WRITE ON CONDITION
SELECT EMPLOYEE.EMP_ID,DEPARTMENT.DEPT_NAME FROM EMPLOYEE NATURAL JOIN DEPARTMENT;

